const onelineTraverser = (row = 0, column = 0) => {
  const right = 3;
  const down = 1;
  return row < mapLayout.length ? onelineTraverser(row + down, (column + right) % mapLayout[0].length) + (mapLayout[row][column] === "#" ? 1 : 0) : 0
}

