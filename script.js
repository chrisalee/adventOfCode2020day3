const traverser = (row = 0, column = 0) => {
  const right = 3;
  const down = 1;
  const width = mapLayout[0].length;
  const numberOfRows = mapLayout.length;
  if(row < numberOfRows) {
    const treeNoTree = mapLayout[row][column] === "#" ? 1 : 0;
    return traverser(row + down, (column + right) % width) + treeNoTree;
  } else {
    return 0;
  }
}

